import React, { useEffect, useState } from 'react';
import Statistics from '../Statistics/Statistics';

const Dashboard = () => {

    const [tasks, setTasks] = useState([]);

    useEffect(() => {
        // Retrieve tasks data from local storage
        const tasksData = JSON.parse(localStorage.getItem('tasks')) || [];
        setTasks(tasksData);
    }, []); // Runs only once when the component mounts

    const pendingTasks = tasks.filter(singleTask => singleTask.status === "pending");
    const progressTasks = tasks.filter(singleTask => singleTask.status === "progress");
    const completedTasks = tasks.filter(singleTask => singleTask.status === "completed");

    return (
        <>
            <div className='flex items-center justify-center'>
                <div className="stats shadow mt-5">

                    <div className="stat">
                        <div className="stat-figure text-secondary">
                            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" className="inline-block w-8 h-8 stroke-current"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M13 16h-1v-4h-1m1-4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z"></path></svg>
                        </div>
                        <div className="stat-title">Tasks</div>
                        <div className="stat-value">{tasks.length}</div>
                    </div>

                    <div className="stat">
                        <div className="stat-figure text-secondary">
                            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" className="inline-block w-8 h-8 stroke-current"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M12 6V4m0 2a2 2 0 100 4m0-4a2 2 0 110 4m-6 8a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4m6 6v10m6-2a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4"></path></svg>
                        </div>
                        <div className="stat-title">Pending</div>
                        <div className="stat-value">{pendingTasks.length}</div>
                    </div>

                    <div className="stat">
                        <div className="stat-figure text-secondary">
                            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" className="inline-block w-8 h-8 stroke-current"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M12 6V4m0 2a2 2 0 100 4m0-4a2 2 0 110 4m-6 8a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4m6 6v10m6-2a2 2 0 100-4m0 4a2 2 0 110-4m0 4v2m0-6V4"></path></svg>
                        </div>
                        <div className="stat-title">In Progress</div>
                        <div className="stat-value">{progressTasks.length}</div>
                    </div>

                    <div className="stat">
                        <div className="stat-figure text-secondary">
                            <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" className="inline-block w-8 h-8 stroke-current"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M5 8h14M5 8a2 2 0 110-4h14a2 2 0 110 4M5 8v10a2 2 0 002 2h10a2 2 0 002-2V8m-9 4h4"></path></svg>
                        </div>
                        <div className="stat-title">Completed</div>
                        <div className="stat-value">{completedTasks.length}</div>
                    </div>

                </div>
            </div>

            <Statistics tasks={tasks} />
        </>
    );
};

export default Dashboard;